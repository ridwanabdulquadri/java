package Semicolon;

public class Cohort15 {
    private Ceiling2 ceiling;
    private Legend Legend;
    private Inem inem;

    public Cohort15(Ceiling2 ceiling, Semicolon.Legend legend, Inem inem) {
        this.ceiling = ceiling;
        Legend = legend;
        this.inem = inem;
    }
    public Ceiling2 getCeiling() {
        return ceiling;
    }

    public Semicolon.Legend getLegend() {
        return Legend;
    }

    public Inem getInem() {
        return inem;
    }
}
